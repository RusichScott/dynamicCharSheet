        function renderLookupSuggestions(row) {
            const input = row?.querySelector(".lookup-name");
            const wrap = input?.closest(".lookup-input-wrap");
            const panel = wrap?.querySelector(".lookup-suggestion-panel");
            if (!input || !wrap || !panel) return;

            const items = getFilteredItems(row);
            const searchTerm = (wrap.dataset.searchTerm || "").trim().toLowerCase();
            const filtered = searchTerm
                ? items.filter((item) => item.name.toLowerCase().includes(searchTerm))
                : items;

            if (!filtered.length) {
                panel.innerHTML = '<div class="lookup-suggestion-empty">Ничего не найдено</div>';
                delete wrap.dataset.activeIndex;
                return;
            }

            const normalizedCurrent = normalizeLookupName(input.value);
            let activeIndex = Number.parseInt(wrap.dataset.activeIndex || "-1", 10);
            if (Number.isNaN(activeIndex) || activeIndex < 0 || activeIndex >= filtered.length) {
                activeIndex = filtered.findIndex((item) => normalizeLookupName(item.name) === normalizedCurrent);
            }
            if (activeIndex < 0) activeIndex = 0;
            wrap.dataset.activeIndex = String(activeIndex);

            panel.innerHTML = filtered
                .map((item, index) => {
                    const cls = index === activeIndex ? " is-active" : normalizeLookupName(item.name) === normalizedCurrent ? " is-current" : "";
                    const safeValue = item.name.replace(/"/g, "&quot;");
                    return `<button class="lookup-suggestion${cls}" type="button" data-lookup-value="${safeValue}">${item.name}</button>`;
                })
                .join("");
        }

        function moveLookupSuggestionActive(row, direction) {
            const wrap = row?.querySelector(".lookup-input-wrap");
            const panel = wrap?.querySelector(".lookup-suggestion-panel");
            const options = panel ? Array.from(panel.querySelectorAll(".lookup-suggestion")) : [];
            if (!wrap || !options.length) return;

            let activeIndex = Number.parseInt(wrap.dataset.activeIndex || "-1", 10);
            if (Number.isNaN(activeIndex)) activeIndex = -1;
            activeIndex += direction;
            if (activeIndex < 0) activeIndex = options.length - 1;
            if (activeIndex >= options.length) activeIndex = 0;
            wrap.dataset.activeIndex = String(activeIndex);
            options.forEach((option, index) => option.classList.toggle("is-active", index === activeIndex));
            options[activeIndex].scrollIntoView({ block: "nearest" });
        }

