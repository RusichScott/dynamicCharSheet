        function enhanceSelectionSearches() {
            document.querySelectorAll("[data-selection-search] .character-input").forEach((input) => {
                if (input.dataset.selectionEnhanced === "true") return;
                input.dataset.selectionEnhanced = "true";

                input.addEventListener("focus", () => {
                    const wrapper = input.closest(".selection-input-wrap");
                    if (wrapper) {
                        delete wrapper.dataset.activeIndex;
                        wrapper.dataset.searchTerm = "";
                    }
                    renderSelectionDropdown(input);
                    closeAllSelectionDropdowns(wrapper);
                    if (!input.disabled) {
                        wrapper?.classList.add("is-open");
                    }
                });

                input.addEventListener("input", () => {
                    const wrapper = input.closest(".selection-input-wrap");
                    if (wrapper) {
                        delete wrapper.dataset.activeIndex;
                        wrapper.dataset.searchTerm = input.value;
                    }
                    renderSelectionDropdown(input);
                    closeAllSelectionDropdowns(wrapper);
                    if (!input.disabled) {
                        wrapper?.classList.add("is-open");
                    }
                });

                input.addEventListener("keydown", (event) => {
                    const wrapper = input.closest(".selection-input-wrap");
                    if (event.key === "ArrowDown") {
                        event.preventDefault();
                        if (!wrapper?.classList.contains("is-open")) {
                            renderSelectionDropdown(input);
                            closeAllSelectionDropdowns(wrapper);
                            wrapper?.classList.add("is-open");
                        }
                        moveSelectionDropdownActive(input, 1);
                    } else if (event.key === "ArrowUp") {
                        event.preventDefault();
                        if (!wrapper?.classList.contains("is-open")) {
                            renderSelectionDropdown(input);
                            closeAllSelectionDropdowns(wrapper);
                            wrapper?.classList.add("is-open");
                        }
                        moveSelectionDropdownActive(input, -1);
                    } else if (event.key === "Enter") {
                        if (wrapper?.classList.contains("is-open")) {
                            event.preventDefault();
                            commitActiveSelection(input);
                        }
                    } else if (event.key === "Escape") {
                        closeSelectionDropdown(input.closest(".selection-input-wrap"));
                    }
                });
            });
        }

