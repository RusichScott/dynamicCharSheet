        function applyBackgroundData() {
            const background = getBackgroundData();
            const alignment = document.querySelector("[data-character-alignment]")?.value || "";
            const backgroundSkillNames = splitBackgroundSkills(background.skills).map((item) => normalizeSkillDisplayName(item));

            document.querySelectorAll(".skill-row .skill-prof").forEach((checkbox) => {
                if (checkbox.dataset.backgroundAuto === "true") {
                    checkbox.checked = false;
                    delete checkbox.dataset.backgroundAuto;
                }
            });

            if (backgroundSkillNames.length) {
                document.querySelectorAll(".skill-row").forEach((row) => {
                    const labelInput = row.querySelector(".skill-name-input");
                    const proficiencyCheckbox = row.querySelector(".skill-prof");
                    if (!labelInput || !proficiencyCheckbox) return;

                    const rowName = normalizeSkillDisplayName(labelInput.value);
                    if (backgroundSkillNames.includes(rowName)) {
                        proficiencyCheckbox.checked = true;
                        proficiencyCheckbox.dataset.backgroundAuto = "true";
                    }
                });
            }

            const traitsField = document.querySelector("[data-background-traits-output]");
            const idealsField = document.querySelector("[data-background-ideals-output]");
            const filteredIdeals = getFilteredBackgroundIdeals(background, alignment);

            if (traitsField) {
                setAutoManagedTextareaValue(traitsField, (background.traits || []).join("\n"));
            }

            if (idealsField) {
                const inferredIdealFromValue = filteredIdeals.find((ideal) => String(idealsField.value || "").startsWith(ideal)) || "";
                const currentAutoIdeal = idealsField.dataset.selectedIdeal || idealsField.dataset.autoValue || inferredIdealFromValue || "";
                const normalizedCurrentAutoIdeal = normalizeLookupName(currentAutoIdeal);
                const matchedCurrentIdeal = filteredIdeals.find((ideal) => normalizeLookupName(ideal) === normalizedCurrentAutoIdeal) || "";
                const nextIdeal = filteredIdeals.length === 1 ? filteredIdeals[0] : matchedCurrentIdeal;
                idealsField.dataset.selectedIdeal = nextIdeal || "";
                setAutoManagedTextareaValue(idealsField, nextIdeal || "");
                renderIdealSuggestions(filteredIdeals, nextIdeal);
            }

            applyRaceTraits();
        }

