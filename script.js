        function updateDerivedValues() {
            syncAllSkillExpertiseStates();

            const scores = getAbilityScores();
            const mods = {};
            const levelInput = document.querySelector("[data-character-level]");
            const parsedLevel = levelInput ? Number.parseInt(levelInput.value, 10) : 1;
            const safeLevel = Math.max(1, Math.min(20, Number.isNaN(parsedLevel) ? 1 : parsedLevel));
            const proficiencyBonus = getProficiencyBonus(safeLevel);

            Object.entries(scores).forEach(([key, score]) => {
                const mod = scoreToModifier(score);
                mods[key] = mod;
                const output = document.querySelector(`[data-ability-mod="${key}"]`);
                if (output) output.value = formatModifier(mod);
            });

            const initiativeOutput = document.querySelector("[data-initiative-output]");
            if (initiativeOutput) {
                initiativeOutput.value = formatModifier(mods.dex || 0);
            }

            const armorClassOutput = document.querySelector("[data-armor-class-output]");
            if (armorClassOutput) {
                armorClassOutput.value = String(getArmorClassTotal(mods.dex || 0));
            }

            const proficiencyOutput = document.querySelector("[data-proficiency-output]");
            if (proficiencyOutput) {
                proficiencyOutput.value = formatModifier(proficiencyBonus);
            }

            document.querySelectorAll("[data-save-output]").forEach((output) => {
                const key = output.dataset.saveOutput;
                const checkbox = document.querySelector(`.save-prof[data-ability-ref="${key}"]`);
                const total = (mods[key] || 0) + (checkbox && checkbox.checked ? proficiencyBonus : 0);
                output.value = formatModifier(total);
            });

            document.querySelectorAll("[data-skill-ability]").forEach((row) => {
                const key = row.dataset.skillAbility;
                const isProficient = Boolean(row.querySelector(".skill-prof")?.checked);
                const hasExpertise = Boolean(row.querySelector(".skill-expertise")?.checked);
                let total = (mods[key] || 0) + (isProficient ? proficiencyBonus : 0);
                if (hasExpertise) {
                    total *= 2;
                }
                const output = row.querySelector(".skill-bonus-input");
                if (output) output.value = formatModifier(total);
            });

            document.querySelectorAll("[data-passive-output]").forEach((output) => {
                const skillLabel = output.dataset.passiveOutput;
                const skillInput = Array.from(document.querySelectorAll(".skill-row .skill-name-input")).find((input) =>
                    input.value.includes(skillLabel)
                );
                const skillRow = skillInput ? skillInput.closest(".skill-row") : null;
                const skillBonus = skillRow ? Number.parseInt(skillRow.querySelector(".skill-bonus-input").value, 10) || 0 : 0;
                output.value = String(10 + skillBonus);
            });

            updateAllLookups();
        }

