        function getArmorClassTotal(dexMod) {
            const shieldRow = document.querySelector('[data-lookup-type="armor"] input.lookup-name[placeholder="Название щита"]')?.closest("[data-lookup-type]");
            const armorRow = document.querySelector('[data-lookup-type="armor"] input.lookup-name[placeholder="Название доспеха"]')?.closest("[data-lookup-type]");
            const shieldItem = findKnowledgeItem("armors", shieldRow ? getConfirmedLookupName(shieldRow) : "");
            const armorItem = findKnowledgeItem("armors", armorRow ? getConfirmedLookupName(armorRow) : "");

            let total = 10 + dexMod;

            if (armorItem && armorItem.armorType !== "Щит") {
                const armorClassText = String(armorItem.armorClass || "");
                const baseMatch = armorClassText.match(/\d+/);
                const baseArmor = baseMatch ? Number.parseInt(baseMatch[0], 10) : 10;

                if (/макс\.\s*\+?2/i.test(armorClassText)) {
                    total = baseArmor + Math.min(dexMod, 2);
                } else if (/Лов/i.test(armorClassText)) {
                    total = baseArmor + dexMod;
                } else {
                    total = baseArmor;
                }
            }

            if (shieldItem) {
                const shieldBonusMatch = String(shieldItem.armorClass || "").match(/[+-]?\d+/);
                if (shieldBonusMatch) {
                    total += Number.parseInt(shieldBonusMatch[0], 10) || 0;
                }
            }

            return total;
        }

