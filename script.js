        function getFilteredItems(row) {
            const type = row.dataset.lookupType;
            const db =
                type === "weapon"
                    ? KNOWLEDGE_BASE.weapons
                    : type === "armor"
                        ? KNOWLEDGE_BASE.armors
                        : type === "equipment"
                            ? KNOWLEDGE_BASE.equipment
                            : KNOWLEDGE_BASE.spells;
            const filters = Object.fromEntries(
                Array.from(row.querySelectorAll("[data-filter]")).map((select) => [select.dataset.filter, select.value])
            );

            return db.filter((item) => {
                if (type === "weapon") {
                    if (filters.weaponType && item.weaponType !== filters.weaponType) return false;
                    if (filters.damageType && item.damageType !== filters.damageType) return false;
                    return true;
                }

                if (type === "armor") {
                    if (filters.armorType && item.armorType !== filters.armorType) return false;
                    if (filters.stealth && item.stealth !== filters.stealth) return false;
                    return true;
                }

                if (type === "equipment") {
                    if (filters.itemType && !(item.itemType || "").includes(filters.itemType)) return false;
                    if (filters.tag && !(item.tag || "").includes(filters.tag)) return false;
                    return true;
                }

                if (filters.levelType && item.levelType !== filters.levelType) return false;
                if (filters.class && !(item.classes || "").includes(filters.class)) return false;
                return true;
            });
        }

