        function getAlignmentTags(alignmentText) {
            const normalized = normalizeLookupName(alignmentText);
            const tags = [];
            if (normalized.includes("закон")) tags.push("законный");
            if (normalized.includes("хаот")) tags.push("хаотичный");
            if (normalized.includes("доб")) tags.push("добрый");
            if (normalized.includes("злой")) tags.push("злой");
            if (normalized.includes("нейт")) tags.push("нейтральный");
            tags.push("любой", "любое");
            return Array.from(new Set(tags));
        }

        function getIdealTag(idealText) {
            const match = String(idealText || "").match(/\(([^()]+)\)\s*$/);
            if (!match) return "";
            return normalizeLookupName(match[1]);
        }

        function getFilteredBackgroundIdeals(background, alignmentText) {
            const ideals = Array.isArray(background?.ideals) ? background.ideals : [];
            if (!ideals.length) return [];

            const allowedTags = getAlignmentTags(alignmentText);
            return ideals.filter((ideal) => {
                const tag = getIdealTag(ideal);
                if (!tag) return true;
                return allowedTags.some((allowed) => tag.includes(allowed));
            });
        }

        function formatRaceTraitsText(traits) {
            const lines = [];

            if (traits.languages.length) {
                lines.push(`Языки: ${traits.languages.join(", ")}`);
            }

            if (traits.resistances.length) {
                lines.push(`Сопротивления: ${traits.resistances.join(", ")}`);
            }

            if (traits.proficiencies.length) {
                lines.push(`Владения: ${traits.proficiencies.join(", ")}`);
            }

            if (traits.notes.length) {
                lines.push(`Примечание: ${traits.notes.join("; ")}`);
            }

            return lines.join("\n");
        }

        function formatCombinedSupportText(raceTraits, background) {
            const lines = [];
            const raceText = formatRaceTraitsText(raceTraits);
            if (raceText) lines.push(raceText);
            if (Array.isArray(background?.proficiencies) && background.proficiencies.length) {
                lines.push(background.proficiencies.join("\n"));
            }
            return lines.join("\n");
        }

        function formatRaceSpeedDisplay(speedText) {
            const raw = String(speedText || "").trim();
            if (!raw) return "";

            const firstPart = raw.split(",")[0].trim();
            const firstNumber = firstPart.match(/\d+/);
            if (firstNumber) {
                return `${firstNumber[0]} фт.`;
            }

            return firstPart
                .replace(/фут(?:ов|а)?/gi, "фт.")
                .replace(/\s+/g, " ")
                .trim();
        }

        function setAutoManagedTextareaValue(field, nextAutoValue) {
            if (!field) return;

            let previousAutoValue = field.dataset.autoValue || "";
            if (!previousAutoValue && nextAutoValue && field.value.startsWith(nextAutoValue)) {
                previousAutoValue = nextAutoValue;
            }
            let manualPart = field.value.trim();

            if (previousAutoValue && field.value.startsWith(previousAutoValue)) {
                manualPart = field.value.slice(previousAutoValue.length).replace(/^\s+/, "");
            }

            const pieces = [];
            if (nextAutoValue) pieces.push(nextAutoValue);
            if (manualPart.trim()) pieces.push(manualPart.trim());

            field.dataset.autoValue = nextAutoValue;
            field.value = pieces.join("\n\n");
        }

