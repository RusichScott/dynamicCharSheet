        function capturePageState() {
            ensurePersistKeys();
            const state = {};

            getPersistableFields().forEach((field) => {
                const key = field.dataset.persistKey;
                if (!key) return;

                if (field.type === "checkbox" || field.type === "radio") {
                    state[key] = { checked: field.checked };
                    return;
                }

                state[key] = { value: field.value };
            });

            return state;
        }

        function savePageState() {
            if (isRestoringPageState) return;
            localStorage.setItem(PAGE_STATE_KEY, JSON.stringify(capturePageState()));
        }

        function resetPageState() {
            localStorage.removeItem(PAGE_STATE_KEY);
            localStorage.removeItem("interactiveCharacterPortrait");
            window.location.reload();
        }

        function loadPageState() {
            ensurePersistKeys();

            const raw = localStorage.getItem(PAGE_STATE_KEY);
            if (!raw) return;

            let saved;
            try {
                saved = JSON.parse(raw);
            } catch (error) {
                console.error("interactiveCharacterSheetState parse error", error);
                return;
            }

            isRestoringPageState = true;

            const fields = getPersistableFields();
            const deferred = [];

            fields.forEach((field) => {
                const key = field.dataset.persistKey;
                const snapshot = key ? saved[key] : null;
                if (!snapshot) return;

                if (field.matches("[data-character-subrace], [data-character-subclass]")) {
                    deferred.push({ field, snapshot });
                    return;
                }

                if (field.type === "checkbox" || field.type === "radio") {
                    field.checked = Boolean(snapshot.checked);
                } else if (typeof snapshot.value !== "undefined") {
                    field.value = snapshot.value;
                }
            });

            updateSubraceOptions();
            updateSubclassOptions();
            applyRaceTraits();
            applyBackgroundData();
            applyClassVitals();

            deferred.forEach(({ field, snapshot }) => {
                if (field.type === "checkbox" || field.type === "radio") {
                    field.checked = Boolean(snapshot.checked);
                } else if (typeof snapshot.value !== "undefined") {
                    field.value = snapshot.value;
                }
            });

            const subraceWrap = document.querySelector("[data-subrace-wrap]");
            if (subraceWrap) {
                const subraceSelect = document.querySelector("[data-character-subrace]");
                subraceWrap.classList.toggle("is-empty", !(subraceSelect && subraceSelect.value));
            }

            const subclassWrap = document.querySelector("[data-subclass-wrap]");
            if (subclassWrap) {
                const subclassSelect = document.querySelector("[data-character-subclass]");
                subclassWrap.classList.toggle("is-empty", !(subclassSelect && subclassSelect.value));
            }

            sanitizeExperienceCurrent();
            updateExperienceDisplay();
            document.querySelectorAll("[data-lookup-type]").forEach((row) => refreshLookupConfirmState(row));
            applyRaceTraits();
            applyBackgroundData();
            applyClassVitals();
            syncAllCustomSelects();
            updateDerivedValues();
            isRestoringPageState = false;
        }

