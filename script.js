        function renderSelectionDropdown(input) {
            const wrapper = input?.closest(".selection-input-wrap");
            const dropdown = wrapper?.querySelector(".selection-dropdown");
            if (!wrapper || !dropdown) return;

            const allOptions = getSelectionSearchOptions(input);
            const searchTerm = (wrapper.dataset.searchTerm || "").trim().toLowerCase();
            const filtered = searchTerm
                ? allOptions.filter((option) => option.toLowerCase().includes(searchTerm))
                : allOptions;

            if (input.disabled) {
                dropdown.innerHTML = "";
                closeSelectionDropdown(wrapper);
                return;
            }

            if (!filtered.length) {
                dropdown.innerHTML = '<div class="selection-empty">Ничего не найдено</div>';
                delete wrapper.dataset.activeIndex;
                return;
            }

            const normalizedCurrent = normalizeLookupName(input.value);
            let activeIndex = Number.parseInt(wrapper.dataset.activeIndex || "-1", 10);
            if (Number.isNaN(activeIndex) || activeIndex < 0 || activeIndex >= filtered.length) {
                activeIndex = filtered.findIndex((option) => normalizeLookupName(option) === normalizedCurrent);
            }
            if (activeIndex < 0) activeIndex = 0;
            wrapper.dataset.activeIndex = String(activeIndex);

            dropdown.innerHTML = filtered
                .map((option, index) => {
                    const isCurrent = normalizeLookupName(option) === normalizedCurrent;
                    const isActive = index === activeIndex ? " is-active" : isCurrent ? " is-current" : "";
                    const safeValue = option.replace(/"/g, "&quot;");
                    return `<button class="selection-option${isActive}" type="button" data-selection-value="${safeValue}">${option}</button>`;
                })
                .join("");
        }

        function moveSelectionDropdownActive(input, direction) {
            const wrapper = input?.closest(".selection-input-wrap");
            const dropdown = wrapper?.querySelector(".selection-dropdown");
            const options = dropdown ? Array.from(dropdown.querySelectorAll(".selection-option")) : [];
            if (!wrapper || !options.length) return;

            let activeIndex = Number.parseInt(wrapper.dataset.activeIndex || "-1", 10);
            if (Number.isNaN(activeIndex)) activeIndex = -1;
            activeIndex += direction;

            if (activeIndex < 0) activeIndex = options.length - 1;
            if (activeIndex >= options.length) activeIndex = 0;

            wrapper.dataset.activeIndex = String(activeIndex);
            options.forEach((option, index) => option.classList.toggle("is-active", index === activeIndex));
            options[activeIndex].scrollIntoView({ block: "nearest" });
        }

