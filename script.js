        document.addEventListener("click", (event) => {
            if (event.target.matches(".stepper-btn")) {
                const targetSelector = event.target.dataset.stepTarget;
                const step = Number.parseInt(event.target.dataset.step, 10) || 0;
                const input = targetSelector ? document.querySelector(targetSelector) : null;
                if (input) {
                    const current = Number.parseInt(input.value, 10);
                    const min = input.min !== "" ? Number.parseInt(input.min, 10) : null;
                    const max = input.max !== "" ? Number.parseInt(input.max, 10) : null;
                    let next = (Number.isNaN(current) ? 0 : current) + step;

                    if (min !== null && !Number.isNaN(min)) next = Math.max(min, next);
                    if (max !== null && !Number.isNaN(max)) next = Math.min(max, next);

                    input.value = String(next);
                    input.dispatchEvent(new Event("input", { bubbles: true }));
                    input.dispatchEvent(new Event("change", { bubbles: true }));
                }
            } else if (event.target.matches(".lookup-confirm-btn")) {
                const row = event.target.closest("[data-lookup-type]");
                if (row) {
                    confirmLookupRow(row);
                    updateDerivedValues();
                    savePageState();
                }
            } else if (event.target.matches(".portrait-clear")) {
                const input = document.querySelector(".portrait-input");
                if (input) input.value = "";
                localStorage.removeItem("interactiveCharacterPortrait");
                setPortrait("");
                savePageState();
            } else if (event.target.matches(".page-reset-button")) {
                resetPageState();
            } else if (event.target.matches("[data-music-toggle]")) {
                const widget = document.querySelector("[data-music-widget]");
                if (widget) {
                    const nextCollapsed = !widget.classList.contains("is-collapsed");
                    localStorage.setItem(MUSIC_WIDGET_STATE_KEY, nextCollapsed ? "1" : "0");
                    applyMusicWidgetState();
                }
            } else if (event.target.matches(".selection-option")) {
                const value = event.target.dataset.selectionValue || "";
                const wrapper = event.target.closest(".selection-input-wrap");
                const input = wrapper?.querySelector(".character-input");
                if (input) {
                    input.value = value;
                    wrapper.dataset.searchTerm = "";
                    closeSelectionDropdown(wrapper);
                    input.dispatchEvent(new Event("change", { bubbles: true }));
                }
            } else if (event.target.matches(".ideal-suggestion-option")) {
                const idealsField = document.querySelector("[data-background-ideals-output]");
                const selectedIdeal = event.target.dataset.idealValue || "";
                if (idealsField) {
                    idealsField.dataset.selectedIdeal = selectedIdeal;
                    setAutoManagedTextareaValue(idealsField, selectedIdeal);
                    renderIdealSuggestions(getFilteredBackgroundIdeals(getBackgroundData(), document.querySelector("[data-character-alignment]")?.value || ""), selectedIdeal);
                    savePageState();
                }
            } else if (event.target.matches(".lookup-suggestion")) {
                const row = event.target.closest("[data-lookup-type]");
                const input = row?.querySelector(".lookup-name");
                if (row && input) {
                    input.value = event.target.dataset.lookupValue || "";
                    updateLookupRow(row);
                    row.querySelectorAll(".lookup-field-area").forEach((field) => autoResizeLookupField(field));
                    refreshLookupConfirmState(row);
                    closeLookupSuggestions(row);
                    updateDerivedValues();
                    savePageState();
                }
            } else if (event.target.matches(".custom-select-option")) {
                const wrapper = event.target.closest(".custom-select");
                const select = Array.from(document.querySelectorAll("select.lookup-select")).find(
                    (item) => item._customSelectWrapper === wrapper
                );
                if (select) {
                    select.value = event.target.dataset.selectValue || "";
                    wrapper.dataset.searchTerm = "";
                    renderCustomSelect(select);
                    closeCustomSelect(wrapper);
                    select.dispatchEvent(new Event("input", { bubbles: true }));
                    select.dispatchEvent(new Event("change", { bubbles: true }));
                }
            }
        });

